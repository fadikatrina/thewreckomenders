def expl_least_misery(item_name):
    return 'The recipe', item_name, 'has been recommended since we think that all of you will enjoy this recipe, ' \
                                    'it is healthy and none of the group members have a problem with it.'


def expl_approval_voting(item_name):
    return 'The recipe', item_name, 'has been recommended since it is approved by the highest amount of group members,'\
                                    'and has the highest rating among the recipes with highest approval rate.'


def expl_most_pleasure(item_name):
    return 'The recipe', item_name, 'has been recommended since some of you will enjoy this recipe, ' \
                                    'because it achieves the highest individual rating.'
